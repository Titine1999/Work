int		ft_str_is_alpha(char *str)
{
	int i;
	int res;

	res = 1;
	i = 0;
	while (str[i] != '\0')
	{
		if (!((str[i] >= 'a' && str[i] <= 'z')
					|| (str[i] >= 'A' && str[i] <= 'Z')))
			res = 0;
		i++;
	}
	return (res);
}
