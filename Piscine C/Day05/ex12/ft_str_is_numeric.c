int			ft_str_is_numeric(char *str)
{
	int i;
	int res;

	res = 1;
	i = 0;
	while (str[i] != '\0')
	{
		if (!(str[i] >= '0' && str[i] <= '9'))
			res = 0;
		i++;
	}
	return (res);
}
