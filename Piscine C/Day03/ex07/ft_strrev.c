char	*ft_strrev(char *str)
{
	int		l;
	int		i;
	char	swp;

	i = 0;
	l = 0;
	while (str[l])
		l++;
	l--;
	while (l >= i)
	{
		swp = str[i];
		str[i] = str[l];
		str[l] = swp;
		l--;
		i++;
	}
	return (str);
}
