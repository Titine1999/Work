int		ft_iterative_factorial(int nb)
{
	int		res;

	res = 1;
	if (nb < 0 || nb > 12)
		res = 0;
	while (nb > 0 && nb < 13)
	{
		res = res * nb;
		nb--;
	}
	return (res);
}
