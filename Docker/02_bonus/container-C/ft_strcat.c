#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int i;
	int l;

	l = 0;
	i = 0;
	while (s1[l] != '\0')
		l++;
	while (s2[i] != '\0')
	{
		s1[l + i] = s2[i];
		i++;
	}
	s1[l + i] = '\0';
	return (s1);
}
