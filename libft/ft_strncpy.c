char	*ft_strcpy(char *dst, const char *src, size_t len)
{
	int		i;

	i = 0;
	while (src[i] != '\0' && i < (len - 1))
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
